public class Dog 
{
	String name;
	int age;
	double weight;
	
	public String getName()
	{
		return name;
	}
	public int getAge()
	{
		return age;
	}
	public Double getWeight()
	{
		return weight;
	}
	public void setName(String dogName)
	{
		name= dogName;
	}
	public void setAge(int dogAge)
	{
		age= dogAge;
	}
	public void setWeight(double dogWeight)
	{
		weight= dogWeight;
	}
}
